				<p>I have been asked why I use a daisy as my logo for Simple Yoga...</p>
				 
				<p>I have always loved daisies. I love  their colour, their simplicity, their freshness. I have a very fond memory of  lying in a field, with arms outstretched, daisies all around me, with my face  upturned to the warmth of the sun. Bliss... Then there is the memory of walking  through fields of moon daisies, with a dog called Biggles, on an organic farm  near to where we used to live. We knew the owners and were allowed to roam  wherever we wanted and only had to call out for Biggles when we arrived, and he  would appear, panting, a huge Airedale, always ready for a walk. With such  lovely memories it is hardly surprising that I chose a daisy to be my logo when  I set up Simple Yoga. It seemed to me to represent the simplicity I was craving  in my yoga and my life. As I write this I am looking out on a field and the rain  is chucking it down but all I have to do is close my eyes and think back to  lying in my field of daisies and I feel more peaceful and relaxed.</p>
				 
				<p>Another love, which I am sure many of  you reading this will share, is the sea. Lying down on your favourite beach  somewhere, all on your own, not another person in sight. Feeling the warmth of  the sand below and the sun above, breathing in that glorious salty air. Hearing  birdsong as your body releases and relaxes as you breathe in and out, in and  out... Becoming aware of the sound of the waves flowing towards the shoreline...  I share this relaxation, or a version of it as every relaxation is slightly  different at the end of each class and I weave in my version of yoga nidra which  leads you further into your peaceful place. I love teaching this relaxation; it  allows the body to fully relax, calming the mind too. My own body and mind relax  as I sit and slowly and intuitively guide the relaxation; if I was lying down I  might nod off!</p>
